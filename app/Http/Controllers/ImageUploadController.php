    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->hasFile('image')) {
            $image = $request->file('image');
            //return $request->image->storeAs('public', 'bitfumes.jpg');
            $loggedinuser = Auth::user()->id;
            $filelocation = Auth::user()->id . time() . '.' . $image->getClientOriginalExtension();
            Storage::putFileAs('public', $request->file('image'), $filelocation);
            $oldfilename = Auth::user()->imagelocation;
            DB::table('users')->where('id', $loggedinuser)->update(['imagelocation' => $filelocation]);
            Storage::delete('public/'.$oldfilename);
            return Redirect::back();
        } else {
            return 'No file selected';
        }
    }

