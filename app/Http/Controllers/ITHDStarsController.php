    {
        $last_updated = new DateTime();

        $ithd_stars = new ITHDStars;
        $ithd_stars->last_updated = $last_updated;
        $ithd_stars->agent_id = request('Agent_Name');
        $ithd_stars->starnumber = request('starnumber');
        $ithd_stars->save();

        return Redirect::back();
